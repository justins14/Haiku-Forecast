def build_tag_index(haikus: dict) -> dict:
    """
    Build an index mapping tags to haiku IDs.
    """
    tag_index = {}
    for haiku_id, haiku in haikus.items():
        for tag in haiku["tags"]:
            if tag not in tag_index:
                tag_index[tag] = []
            tag_index[tag].append(haiku_id)
    return tag_index